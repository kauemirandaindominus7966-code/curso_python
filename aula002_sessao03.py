valor1 = int(input('Digite um valor: '))
valor2 = int(input('Digite outro valor: '))

if valor1 < valor2:
    print(f'O valor1 {valor1} é menor que o valor2 {valor2}')
elif valor1 > valor2:
    print(f'O valor1 {valor1} é maior que o valor2 {valor2}')
else:
    print(f'O valor1 {valor1} é igual que o valor2 {valor2}')